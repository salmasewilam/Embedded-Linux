//Create a table for AscII code

#include <iostream>

int main()
{
    std:: cout<<"ASCII code Table\n";
    std:: cout<<"----------------\n";
    std:: cout<<"| Char | ASCII |\n";
    std:: cout<<"----------------\n";
    for (unsigned char i=0;i<128;i++)
    {
        std:: cout<<"|  "<<'i'<<"  |\n";
    }

    return 0;
}